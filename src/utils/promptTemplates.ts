/**
 * 替换模板中的 {{变量名}} 占位符
 */
export function renderPrompt(
  template: string,
  variables: Record<string, string>
): string {
  let result = template
  for (const [key, value] of Object.entries(variables)) {
    const placeholder = key.startsWith('{{') ? key : `{{${key}}}`
    result = result.replaceAll(placeholder, value || '')
  }
  return result
}

